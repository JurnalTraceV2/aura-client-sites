import crypto from 'crypto';
import fs from 'fs';
import path from 'path';

const ROOT_DIR = process.cwd();
const ARTIFACTS_DIR = path.join(ROOT_DIR, 'artifacts');
const PUBLIC_DOWNLOADS_DIR = path.join(ROOT_DIR, 'public', 'downloads');
const PUBLIC_LAUNCHER_DIR = path.join(ROOT_DIR, 'public', 'launcher');
const RELEASE_METADATA_PATH = path.join(ARTIFACTS_DIR, 'release-metadata.env');

const CANONICAL_CLIENT_NAME = 'Aura.jar';
const CANONICAL_LAUNCHER_NAME = 'AuraLauncher.exe';

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function hasFile(filePath) {
  try {
    return fs.statSync(filePath).isFile();
  } catch {
    return false;
  }
}

function listFilesByExtensions(dirPath, extensions) {
  if (!hasFile(path.join(dirPath, '.keep')) && !fs.existsSync(dirPath)) {
    return [];
  }

  return fs
    .readdirSync(dirPath, { withFileTypes: true })
    .filter((entry) => entry.isFile())
    .map((entry) => path.join(dirPath, entry.name))
    .filter((filePath) => extensions.includes(path.extname(filePath).toLowerCase()))
    .sort((left, right) => fs.statSync(right).mtimeMs - fs.statSync(left).mtimeMs);
}

function resolveArtifactSource(preferredName, extensions) {
  const preferredPath = path.join(ARTIFACTS_DIR, preferredName);
  if (hasFile(preferredPath)) {
    return preferredPath;
  }

  return listFilesByExtensions(ARTIFACTS_DIR, extensions)[0] || '';
}

function computeSha256Hex(filePath) {
  const buffer = fs.readFileSync(filePath);
  return crypto.createHash('sha256').update(buffer).digest('hex');
}

function inferContentType(fileName) {
  const lower = String(fileName || '').toLowerCase();
  if (lower.endsWith('.jar')) {
    return 'application/java-archive';
  }
  if (lower.endsWith('.exe')) {
    return 'application/vnd.microsoft.portable-executable';
  }
  if (lower.endsWith('.zip')) {
    return 'application/zip';
  }
  return 'application/octet-stream';
}

function formatVersionTimestamp(mtimeMs) {
  const date = new Date(mtimeMs);
  const pad = (value) => String(value).padStart(2, '0');

  return [
    date.getUTCFullYear(),
    pad(date.getUTCMonth() + 1),
    pad(date.getUTCDate())
  ].join('') + '-' + [
    pad(date.getUTCHours()),
    pad(date.getUTCMinutes()),
    pad(date.getUTCSeconds())
  ].join('');
}

function sanitizeVersionPart(value, fallback) {
  return String(value || '')
    .trim()
    .replace(/[^a-z0-9]+/gi, '-')
    .replace(/^-+|-+$/g, '')
    || fallback;
}

function buildVersion(canonicalName, filePath, sha256) {
  const versionBase = sanitizeVersionPart(path.basename(canonicalName, path.extname(canonicalName)), 'artifact');
  return `${versionBase}-${formatVersionTimestamp(fs.statSync(filePath).mtimeMs)}-${sha256.slice(0, 12)}`;
}

function toRepoRelative(filePath) {
  return path.relative(ROOT_DIR, filePath).split(path.sep).join('/');
}

function buildMetadata(prefix, sourcePath, canonicalName) {
  if (!sourcePath) {
    const fallbackPath = toRepoRelative(path.join(ARTIFACTS_DIR, canonicalName));
    return [
      `${prefix}_ARTIFACT_PATH=${fallbackPath}`,
      `${prefix}_ARTIFACT_NAME=${canonicalName}`,
      `${prefix}_ARTIFACT_CONTENT_TYPE=${inferContentType(canonicalName)}`,
      `${prefix}_VERSION=`,
      `${prefix}_SHA256=`,
      `${prefix}_SIZE=0`,
      `${prefix}_ARTIFACT_URL=`
    ];
  }

  const sha256 = computeSha256Hex(sourcePath);
  const size = fs.statSync(sourcePath).size;
  const contentType = inferContentType(canonicalName);
  const version = buildVersion(canonicalName, sourcePath, sha256);

  return [
    `${prefix}_ARTIFACT_PATH=${toRepoRelative(sourcePath)}`,
    `${prefix}_ARTIFACT_NAME=${canonicalName}`,
    `${prefix}_ARTIFACT_CONTENT_TYPE=${contentType}`,
    `${prefix}_VERSION=${version}`,
    `${prefix}_SHA256=${sha256}`,
    `${prefix}_SIZE=${size}`,
    `${prefix}_ARTIFACT_URL=`
  ];
}

function writeReleaseMetadata(clientSource, launcherSource) {
  ensureDir(ARTIFACTS_DIR);

  const generatedAt = new Date().toISOString();
  const lines = [
    `# Auto-generated by scripts/prepare-artifacts.mjs at ${generatedAt}`,
    '# Drop the latest client .jar and launcher .exe into artifacts/ and redeploy.',
    ...buildMetadata('CLIENT', clientSource, CANONICAL_CLIENT_NAME),
    '',
    ...buildMetadata('LAUNCHER', launcherSource, CANONICAL_LAUNCHER_NAME),
    ''
  ];

  fs.writeFileSync(RELEASE_METADATA_PATH, `${lines.join('\n')}`, 'utf8');
}

function syncPublicLauncher(launcherSource) {
  const publicLauncherPath = path.join(PUBLIC_DOWNLOADS_DIR, CANONICAL_LAUNCHER_NAME);
  ensureDir(PUBLIC_DOWNLOADS_DIR);
  ensureDir(PUBLIC_LAUNCHER_DIR);

  if (!launcherSource) {
    if (hasFile(publicLauncherPath)) {
      fs.rmSync(publicLauncherPath, { force: true });
    }
    return '';
  }

  fs.copyFileSync(launcherSource, publicLauncherPath);

  const launcherVersion = buildVersion(CANONICAL_LAUNCHER_NAME, launcherSource, computeSha256Hex(launcherSource));
  fs.writeFileSync(path.join(PUBLIC_LAUNCHER_DIR, 'version.txt'), `${launcherVersion}\n`, 'utf8');

  return publicLauncherPath;
}

function main() {
  const clientSource = resolveArtifactSource(CANONICAL_CLIENT_NAME, ['.jar']);
  const launcherSource = resolveArtifactSource(CANONICAL_LAUNCHER_NAME, ['.exe']);

  writeReleaseMetadata(clientSource, launcherSource);
  const publicLauncherPath = syncPublicLauncher(launcherSource);

  const summary = {
    clientSource: clientSource ? toRepoRelative(clientSource) : 'missing',
    launcherSource: launcherSource ? toRepoRelative(launcherSource) : 'missing',
    publicLauncherPath: publicLauncherPath ? toRepoRelative(publicLauncherPath) : 'removed',
    releaseMetadataPath: toRepoRelative(RELEASE_METADATA_PATH)
  };

  console.log(JSON.stringify(summary, null, 2));
}

main();
